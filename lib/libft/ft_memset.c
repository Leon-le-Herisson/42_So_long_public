#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*a;
	size_t			i;

	i = 0;
	a = s;
	while (i < n)
	{
		*a = (unsigned char) c;
		i++;
		a++;
	}
	return (s);
}
